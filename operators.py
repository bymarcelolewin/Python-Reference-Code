print(1+1)
print(4*5)

#Exponent
print(5**2)

# Division always returns a float number
print(20/5)
print(20/6)

# Returns remainder
print(20%2)
print(30%25)

#string operators (Can only use + and *)
print("Hello " + "Marcelo")
print("Hello " * 4)

#Comparison Operators
print(True == True)
print(True != False)
print (4<56)
print(5<=5)
print(5>2)
print(5>=2)

#Logical Operators
print(True and True)
print(True and False)
print(True or False)
print(False or False)
print(not True)
print(not False)

#membership Operators
print (1 in [1,2,3,4])
print (2 in ['A', 'B'])
print (5 not in [3,4,7])
print ('cat' in 'My cat is nice.')
print ('cat' in 'catatonic')